const colorMap: Record<number, string> = {
  0: 'transparent',
  1: '#e5e7eb', // Hair - light gray/white
  2: '#fcd9b6', // Skin tone
  3: '#1f2937', // Coat - dark gray
  4: '#f3f4f6', // Shirt - white
  5: '#374151', // Pants - gray
  6: '#111827', // Shoes - black
  7: '#dc2626', // Tie - red
  8: '#1f2937', // Eyes - dark
}

const PIXEL_SIZE = 6

export function LoadingPage() {
  const [frame, setFrame] = useState(0)
  const [position, setPosition] = useState(-150)

  useEffect(() => {
    // Animation frame cycling
    const frameInterval = setInterval(() => {
      setFrame((prev) => (prev + 1) % frames.length)
    }, 100)

    // Movement animation
    const moveInterval = setInterval(() => {
      setPosition((prev) => {
        if (prev > window.innerWidth + 150) {
          return -150
        }
        return prev + 8
      })
    }, 16)

    return () => {
      clearInterval(frameInterval)
      clearInterval(moveInterval)
    }
  }, [])

  const currentFrame = frames[frame]

  return (
    <div className="fixed inset-0 flex items-center justify-center bg-background z-50 overflow-hidden">
      <div
        className="absolute"
        style={{
          left: position,
          top: '50%',
          transform: 'translateY(-50%)',
        }}
      >
        <div
          style={{
            display: 'grid',
            gridTemplateColumns: `repeat(16, ${PIXEL_SIZE}px)`,
            gridTemplateRows: `repeat(24, ${PIXEL_SIZE}px)`,
            imageRendering: 'pixelated',
          }}
        >
          {currentFrame.flat().map((pixel, index) => (
            <div
              key={index}
              style={{
                width: PIXEL_SIZE,
                height: PIXEL_SIZE,
                backgroundColor: colorMap[pixel],
              }}
            />
          ))}
        </div>
      </div>
    </div>
  )
}
